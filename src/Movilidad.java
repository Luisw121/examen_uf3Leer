public class Movilidad {
    int idMovilidad;
    String nombre;
    String pais;
    int dias;

    public Movilidad(int idMovilidad, String nombre, String pais, int dias) {
        this.idMovilidad = idMovilidad;
        this.nombre = nombre;
        this.pais = pais;
        this.dias = dias;
    }
}
